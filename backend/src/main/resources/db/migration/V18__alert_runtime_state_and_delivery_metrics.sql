CREATE TABLE alert_delivery_metric (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    alert_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    username VARCHAR(100) NOT NULL,
    role VARCHAR(32) NOT NULL,
    client_session_id VARCHAR(128) NULL,
    client_rendered_at DATETIME(3) NULL,
    ack_received_at DATETIME(3) NOT NULL,
    latency_ms BIGINT NULL,
    invalid_reason VARCHAR(64) NULL,
    created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
    UNIQUE INDEX uk_alert_delivery_user (alert_id, user_id),
    INDEX idx_alert_delivery_ack (ack_received_at),
    INDEX idx_alert_delivery_alert (alert_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='浏览器完成告警呈现的确认记录，不代表业务确认';
